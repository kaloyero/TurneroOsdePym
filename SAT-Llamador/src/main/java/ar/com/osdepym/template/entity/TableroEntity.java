package ar.com.osdepym.template.entity;

public class TableroEntity {
	
	private String usuariosConectados;
	private String sector;
	private String tiempoEspera;
	private String clientesEspera;
	private String tiempoAtencion;
	
	public String getUsuariosConectados() {
		return usuariosConectados;
	}
	public void setUsuariosConectados(String usuariosConectados) {
		this.usuariosConectados = usuariosConectados;
	}
	public String getSector() {
		return sector;
	}
	public void setSector(String sector) {
		this.sector = sector;
	}
	public String getTiempoEspera() {
		return tiempoEspera;
	}
	public void setTiempoEspera(String tiempoEspera) {
		this.tiempoEspera = tiempoEspera;
	}
	public String getTiempoAtencion() {
		return tiempoAtencion;
	}
	public void setTiempoAtencion(String tiempoAtencion) {
		this.tiempoAtencion = tiempoAtencion;
	}
	public String getClientesEspera() {
		return clientesEspera;
	}
	public void setClientesEspera(String clientesEspera) {
		this.clientesEspera = clientesEspera;
	}
	
	
	
}
